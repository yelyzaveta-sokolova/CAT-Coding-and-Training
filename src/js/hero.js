const SCROLL_BTN_HERO = document.querySelector('.btn-scroll-top');

window.addEventListener('scroll', () => {
  if (window.scrollY > 300) {
    SCROLL_BTN_HERO.classList.remove('is-btnshow-hide');
  } else {
    SCROLL_BTN_HERO.classList.add('is-btnshow-hide');
  }
});

SCROLL_BTN_HERO.onclick = () => {
  window.scrollTo({
    top: 0,
    behavior: 'smooth',
  });
};
